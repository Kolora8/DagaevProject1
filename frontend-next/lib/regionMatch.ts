export function normalize(s: string): string {
  return (s || "")
    .toLowerCase()
    .replace(/ё/g, "е")
    .replace(/\s+/g, " ")
    .trim();
}

// Находит элемент по имени региона из GeoJSON (точное, по карте имён, либо нечёткое).
export function matchByName<T extends { name: string }>(
  geoName: string,
  items: T[]
): T | undefined {
  const dbName = NAME_MAP[geoName] || geoName;
  const target = normalize(dbName);
  let r = items.find((x) => normalize(x.name) === target);
  if (!r) {
    const g = normalize(geoName);
    r = items.find(
      (x) => normalize(x.name).includes(g) || g.includes(normalize(x.name))
    );
  }
  return r;
}
